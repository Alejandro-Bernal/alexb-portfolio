export function Neofetch() {
    const lines = useMemo(() => mooseArt.split("\n"), []);
    const [visible, setVisible] = useState(0);

    useEffect(() => {
        let mounted = true;
        let idx = 0;
        let timer: ReturnType<typeof setTimeout> | null = null;

        const step = () => {
            if (!mounted) return;
            idx = idx + 1;
            setVisible(idx);
            if (idx < lines.length) {
                // variable speed: slightly slower for top lines
                timer = setTimeout(step, 25);
            }
        };

        // start after a short pause so the note can be read
        timer = setTimeout(step, 300);

        return () => {
            mounted = false;
            if (timer) clearTimeout(timer);
        };
    }, [lines]);

    return (
        <div className="neofetch-output">
            <p className="neofetch-note">
                Mini Moose has entered the terminal...
            </p>
            <pre className="ascii-moose" aria-hidden="true">
                {lines.slice(0, visible).join("\n")}
            </pre>
        </div>
    );
}

export default Neofetch;
